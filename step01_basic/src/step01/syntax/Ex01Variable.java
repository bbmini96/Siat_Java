package step01.syntax;

public class Ex01Variable {

	public static void main(String[] args) {
		// 기본타입 Primitive Type
		
		// 정수(byte,short, int, long)
		byte vb1 = -128;
//		byte vb2 = 128;		// Error	
		
		short vs1 = 128;
		
		int vi1 = 10;
		int vi2 = 0B1010;
		
		long vl1 = 10;
		long vl2 = 10000000000L;
		
		// 실수(float,double)
		float vf1 = 3.14f;
		double vd1 = 3.14;
		
		// 문자(char)
		char va1 = 'A';
		char va2 = 65;
		
		// 불리언(boolean)
		boolean vt = true;
		boolean vf = false;

		
		// 문자열(string)
		String str = "Hello, Java";
		
		System.out.println(str);
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
